package obejct;

public class GoOff {
	private String wdate;	// 날짜
	private String eid;		// 아이디
	private String ename;	// 이름
	private String gotime;	// 출근시간
	private String offtime;	// 퇴근시간
	
	public GoOff() {}
	
	public GoOff(String wdate, String eid, String ename, String gotime, String offtime) {
		super();
		this.wdate	= wdate;
		this.eid	= eid;
		this.ename	= ename;
		this.gotime	= gotime;
		this.offtime= offtime;
	}
	
	public String getWdate() {
		return wdate;
	}
	public void setWdate(String wdate) {
		this.wdate = wdate;
	}
	public String getEid() {
		return eid;
	}
	public void setEid(String eid) {
		this.eid = eid;
	}
	public String getEname() {
		return ename;
	}
	public void setEname(String ename) {
		this.ename = ename;
	}
	public String getGotime() {
		return gotime;
	}
	public void setGotime(String gotime) {
		this.gotime = gotime;
	}
	public String getOfftime() {
		return offtime;
	}
	public void setOfftime(String offtime) {
		this.offtime = offtime;
	}
}
